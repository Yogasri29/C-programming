/*writing a code on bitwise operators*/
#include <stdio.h>

int main() {
    int a = 5;     
    int b = 9;     

    printf("a = %d, b = %d\n", a, b);

    printf("a & b  = %d\n", a & b);       
    printf("a | b   = %d\n", a | b);      
    printf("a ^ b  = %d\n", a ^ b);      
    printf("~a    = %d\n", ~a);            
    printf("a << 1  = %d\n", a << 1);   
    printf("b >> 1 = %d\n", b >> 1);  


}
